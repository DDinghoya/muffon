<template>
  <template v-if="tags">
    <BaseTagsList
      :tags="tags"
      :isMore="isMore"
      isWithIcon
      @moreClick="handleMoreClick"
    />

    <BaseVideoTagsModal
      v-if="isMore"
      ref="modal"
      :videoId="videoId"
    />
  </template>
</template>

<script>
import BaseTagsList from '*/components/lists/tags/BaseTagsList.vue'
import BaseVideoTagsModal
  from '*/components/modals/video/BaseVideoTagsModal.vue'

export default {
  name: 'TagsSection',
  components: {
    BaseTagsList,
    BaseVideoTagsModal
  },
  props: {
    videoData: {
      type: Object,
      required: true
    }
  },
  computed: {
    tags () {
      return this.videoData.tags
    },
    isMore () {
      return this.videoData.with_more?.tags
    },
    videoId () {
      return this.videoData.youtube_id
    }
  },
  methods: {
    handleMoreClick () {
      this.$refs.modal.show()
    }
  }
}
</script>

